public class MultiDimentionalArray {
    public static void main(String[] args) {
        int[][] dimentionalArray = {
            {0,1,2,3},
            {7,8,9,6},
            {8,5,2,1}
        };

        dimentionalArray[0][3] = 981523;    
        System.out.println(dimentionalArray[0][3]);
        System.out.println(dimentionalArray[1][3]);
    }
}
