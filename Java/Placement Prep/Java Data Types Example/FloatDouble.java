class FloatDouble {
    public static void main(String[] args) {
        long exampleOfLong = 314159265358974944L;
        float exampleOfFloat = 3.14159265358974944f;
        double exampleOfDouble = 3.14159265358974944d;

        System.out.println(exampleOfLong);
        System.out.println(exampleOfFloat);
        System.out.println(exampleOfDouble);
    }
}
